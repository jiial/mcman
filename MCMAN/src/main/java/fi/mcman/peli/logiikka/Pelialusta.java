
package fi.mcman.peli.logiikka;

public class Pelialusta {
    private int leveys;
    private int korkeus;

    public Pelialusta(int leveys, int korkeus) {
        this.leveys = leveys;
        this.korkeus = korkeus;
    }

    public int getKorkeus() {
        return korkeus;
    }

    public int getLeveys() {
        return leveys;
    }
    
    
}
